    @Override
    public void mouseClicked(MouseEvent e) {
        if (e.getButton() == MouseEvent.BUTTON1 && e.getClickCount() == 2) {
            Object o = jlistTables.getSelectedValue();
            if (o != null) {
                TableFrame frame = (TableFrame) o;
                MainWindow.getInstance().showFrame( frame );
            } 
        }
    }

